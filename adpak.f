C
C     ********** CALCULATE F(N,M) ( M > N ) **********
C
C
C     BEGIN BY EVALUTING HYDROGENIC F(N,M) EQUATION, WITH
C     EXPLICIT VALUES SUBSTITUTED FOR N < 5, M <= 5.
C
C
      DO 10 JN = 1 , IVALNC
      IIJM = MAX0 ( JN + 1 , IVALNC )
      ZN = FLOAT(JN)

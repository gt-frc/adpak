     &                   ( 1./FLOAT(JN)**2 - 1./FLOAT(JM)**2 )
      ZRATIO = ZEHYDR / ENM(JQ,JN,JM)
      IF(ZRATIO .GT. 1.0) ZRATIO = 1.0
      ZYNPN = ZY0(JN) + ZY1(JN) * APN(JQ,JN) + ZY2(JN) * APN(JQ,JN)**2
C
      FNM(JQ,JN,JM) = FNM(JQ,JN,JM) * ZRATIO * ZYNPN
C
   20 CONTINUE

      FNM(JQ,1,4) = 0.0290
      FNM(JQ,1,5) = 0.0139
      FNM(JQ,2,3) = 0.6370
      FNM(JQ,2,4) = 0.1190
      FNM(JQ,2,5) = 0.0443
      FNM(JQ,3,4) = 0.8408
      FNM(JQ,3,5) = 0.1499
      FNM(JQ,4,5) = 1.037

C     ******************************************************
C     ***** LOOP FOR ALL SPECIES EXCEPT FULLY STRIPPED *****
C     ******************************************************
C
C
      DO 100 JQ = 1 , NUCZ
C
      ZCHG = FLOAT(JQ-1)
      IBOUND = NUCZ - JQ + 1
      IVALNC = NVALNC(JQ)

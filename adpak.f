C
C     MULTIPLY BY ELECTRON POPULATION OF SHELL N AND FRACTIONAL
C     VACANCY OF SHELL M
C
C
      DO 30 JN = 1 , 5
      DO 30 JM = 1 , 10
      FNM(JQ,JN,JM) = APN(JQ,JN) * AQN(JQ,JM) * FNM(JQ,JN,JM)
   30 CONTINUE
